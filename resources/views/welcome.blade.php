                                    <p>I 2011 og starten af 2012 stod der renovering på planen i Andelsforeningen Sønder lindbjerg II.<p>

                                    <p>Alle døre og vinduer blev udskiftet til mere energirigtige løsninger.
                                        "Tårnene" er blevet efterisoleret og skifferpladerne er skiftet ud.
                                        Med disse tiltag ser vi alle frem til en bedre varmeøkonomi i andelene.</p>

                                    <p>Vores haveanlæg og stakitter rundt om vores haver er også blevet renoveret,
                                        så det hele fremstår nyt og moderne. Noget vi har fået meget ros for fra forbipasserende.
                                        Vi håber, at den nye have indbyder til mere socialt samvær.</p>

                                    <p>Indenfor har badeværelserne fået nye silikonefuger overalt,
